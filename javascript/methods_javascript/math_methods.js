// Math method in javascript 


// PI value 

console.log(Math.PI);


// square root of given number it will return 

console.log(Math.sqrt(25));

//round method

// round up the value to its nearest

console.log(Math.round(4.3)); //4
console.log(Math.round(4.8)); //5


// floor method 

// always round down

console.log(Math.floor(4.5)) //4


// ceil method 

// always round up 

console.log(Math.ceil(4.4)) //5

// pow method 

// power that number with specified number 

console.log(Math.pow(2,4));  //2*2*2*2 = 16

// min & max method 

console.log(Math.min(3,5)); //3
console.log(Math.max(3,5)); //5


//abs method

console.log(Math.abs(-12)); //12

